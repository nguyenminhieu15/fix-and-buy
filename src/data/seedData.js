function ensureMinimumCatalog(seedProducts) {
  const colors = ["Titan Gray", "Ocean Blue", "Forest Green", "Pearl White", "Jet Black", "Sunrise Gold"];
  const storage = ["128GB", "256GB", "512GB"];
  const result = [...seedProducts];
  const base = seedProducts.filter((item) => item.type !== "service");
  let index = 0;
  while (result.length < 112) {
    const source = base[index % base.length];
    const variantNo = Math.floor(index / base.length) + 1;
    const color = colors[index % colors.length];
    const memory = storage[index % storage.length];
    const variantName = source.type === "phone"
      ? `${source.name} ${memory} ${color}`
      : `${source.name} - ${color}`;
    result.push({
      ...source,
      sku: `${source.sku}-X${variantNo}`,
      slug: `${source.slug}-x${variantNo}`,
      name: variantName,
      colorLabel: color,
      price: Math.max(99000, Math.round(Number(source.price || 0) * (1 + ((index % 5) - 2) * 0.03))),
      comparePrice: Math.max(0, Math.round(Number(source.comparePrice || source.price || 0) * (1 + ((index % 4) + 1) * 0.02))),
      stock: Math.max(4, Number(source.stock || 0) + (index % 8)),
      featured: false,
      bestseller: false,
      shortDescription: `${source.brand} • ${source.type === "phone" ? "smartphone" : "linh kiện"} • giao hàng toàn quốc • thanh toán rõ ràng.`,
      description: `${variantName} thuộc danh mục Fix and Buy Smartphone, phù hợp mua mới, mua kèm phụ kiện hoặc thay thế linh kiện khi cần.`,
      tags: Array.from(new Set([...(source.tags || []), "Fix & Buy", color, memory])),
      specs: {
        ...(source.specs || {}),
        "Cấu hình": source.type === "phone" ? memory : "Phụ kiện chính hãng",
        "Màu sắc": color,
        "Bộ nhớ": memory,
      },
    });
    index += 1;
  }
  return result;
}

